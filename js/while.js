//Repetição while
let i = 0;

while(i < 10){
    i = i + 1;
    console.log(`o número é ${i}`);
}

let cont;
cont = 0
do{
    cont++
    console.log(`o número é `);
}while(cont<=10);

//Repetição for
let c;

for(c=0;c<10;c++){
    console.log(c)
}

//for no for
for(let linha=0;linha<5;linha++){
    for(let coluna=0;coluna<2;coluna++){
        console.log(`A linha é ${linha}, e a coluna é ${coluna}.`)
    }
}

